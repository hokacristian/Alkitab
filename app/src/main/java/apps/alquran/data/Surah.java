    public String getArti() {
        return arti;
    }

    public String getAsma() {
        return asma;
    }

    public int getAyat() {
        return ayat;
    }

    public String getNama() {
        return nama;
    }

    public String getType() {
        return type;
    }

    public String getUrut() {
        return urut;
    }

    public String getAudio() {
        return audio;
    }

    public String getNomor() {
        return nomor;
    }

    public String getRukuk() {
        return rukuk;
    }

    public String getKeterangan() {
        return keterangan;
    }

